    private void tbl_locac_registroGeralMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_locac_registroGeralMouseClicked
        List<Locacao> locacao_aux = ImobiliariaIU.imobiliaria.ListaLocações();

        int indice = tbl_locac_registroGeral.getSelectedRow();

        if (indice >= 0 && indice <= locacao_aux.size()) {
            if (locacao_aux.get(indice).getEstado() == LocacaoEstado.Disponivel) {
                btn_locac_anunciar.setEnabled(true);
            } else if (locacao_aux.get(indice).getEstado() == LocacaoEstado.Alugar) {
                btn_locac_alugar.setEnabled(true);
            }
            if (locacao_aux.get(indice).getEstado() == LocacaoEstado.Alugado) {
                btn_locac_despejar.setEnabled(true);
            }
        }

        if (locacao_aux.get(indice).getEstado() == LocacaoEstado.Alugado) {
            cons_locac_inquilino.setText(locacao_aux.get(indice).getInquilino());
        }

        btn_locac_cancelar.setEnabled(true);
        btn_locac_LimparLocacao.setEnabled(true);
    }//GEN-LAST:event_tbl_locac_registroGeralMouseClicked

