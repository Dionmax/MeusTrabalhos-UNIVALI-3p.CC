    private void btn_locac_LimparLocacaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_locac_LimparLocacaoActionPerformed
        int indice = tbl_locac_registroGeral.getSelectedRow();
        List<Locacao> locacao_aux = ImobiliariaIU.imobiliaria.ListaLocações();

        if (indice >= 0 && indice < locacao_aux.size()) {
            if (locacao_aux.get(indice).getEstado() != LocacaoEstado.Alugado) {
                locacao_aux.remove(locacao_aux.get(indice));
            } else {
                JOptionPane.showMessageDialog(rootPane, "Meio complicado apagar esse registro com alguém morando nele.", "Error!", JOptionPane.ERROR_MESSAGE);
            }
        }
        carregarTabelaLocacao();
    }//GEN-LAST:event_btn_locac_LimparLocacaoActionPerformed

    private void btn_locac_despejarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_locac_despejarActionPerformed
        int indice = tbl_locac_registroGeral.getSelectedRow();
        List<Locacao> locacao_aux = ImobiliariaIU.imobiliaria.ListaLocações();

        if (indice >= 0 && indice < locacao_aux.size()) {
            if (locacao_aux.get(indice).getEstado() == LocacaoEstado.Alugado) {
                locacao_aux.get(indice).alugar(locacao_aux.get(indice).getValor());
            }
        }

        locacao_aux.get(indice).setInquilino("Vazio", "Vazio");
        carregarTabelaLocacao();
    }//GEN-LAST:event_btn_locac_despejarActionPerformed

    private void btn_locac_alugar_okActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_locac_alugar_okActionPerformed
        int indice = tbl_locac_registroGeral.getSelectedRow();
        String inquilino_aux = cons_locac_inquilino.getText();
        String CPF_aux = cons_locac_CPF.getText();

        List<Locacao> locacao_aux = ImobiliariaIU.imobiliaria.ListaLocações();

        if (indice >= 0 && indice <= locacao_aux.size()) {
            if (inquilino_aux != "" && CPF_aux != "") {
                if (locacao_aux.get(indice).getEstado() == LocacaoEstado.Alugar) {
                    locacao_aux.get(indice).finalizar(inquilino_aux, CPF_aux);
                }
            }
        }

        controleInterface();
    }//GEN-LAST:event_btn_locac_alugar_okActionPerformed

    private void btn_locac_anunciar_okActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_locac_anunciar_okActionPerformed

        int indice = tbl_locac_registroGeral.getSelectedRow();
        String valor_aux = cons_locac_preco.getText();
        List<Locacao> locacao_aux = ImobiliariaIU.imobiliaria.ListaLocações();

        if (indice >= 0 && indice <= locacao_aux.size()) {
            if (valor_aux != "") {
                if (locacao_aux.get(indice).getEstado() == LocacaoEstado.Disponivel) {
                    locacao_aux.get(indice).alugar(valor_aux);
                }
            }
        }

        carregarTabelaLocacao();

        modo = "navegar";
        controleInterface();
    }//GEN-LAST:event_btn_locac_anunciar_okActionPerformed

